def pick(pool, idx, mul, add=0):
    return pool[(idx * mul + add) % len(pool)]


def chapter_text(idx, chap_idx, heroine, hero, setting, identity, hook, skill, villain, ally, tone):
    scene = pick(SCENES, idx + chap_idx, 7, chap_idx)
    a, b, c, d = pick(SHAPES, idx, 5, chap_idx)
    moral = pick(CHOICES, idx, 11, chap_idx)
    p1 = f'{heroine}站在{scene}里，第一眼看到的是灯，第二眼看到的是局。她如今的身份是{setting}里的{identity}，压在头顶的死线只有一句话——{hook}。没人给她缓冲，她也不准备哭。她先把每张脸记住，再把每句废话拆开听。因为在这种地方，真正致命的从来不是声音大的人，而是笑着递刀的人。'
