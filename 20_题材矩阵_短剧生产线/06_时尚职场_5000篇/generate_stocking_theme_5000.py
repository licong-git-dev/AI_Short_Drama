def make_story(idx: int) -> str:
    setting = pick(SETTINGS, idx, 3)
    identity = pick(IDENTITIES, idx, 5)
    hook = pick(HOOKS, idx, 7)
    skill = pick(SKILLS, idx, 11)
    villain = pick(VILLAINS, idx, 13)
    ally = pick(ALLIES, idx, 17)
    tone = pick(TONES, idx, 19)
    heroine = pick(NAMES_F, idx, 23)
    hero = pick(NAMES_M, idx, 29)
    title_core = f'{setting}{identity}{tone}'
    title = f'{title_core}第{idx:05d}篇'
    intro = f'{heroine}身在{setting}，是人人看低一眼的{identity}。她面前的第一道死线是“{hook}”，手里唯一能救命的，是{skill}。可真正决定她能不能翻身的，不是运气，而是她能否在“{villain}”织成的局里，先保住自己，再把所有人踩回原位。'
    parts = [f"# 《{title}》\n\n## 基本信息\n- 题材：都市 / {tone}\n- 背景：{setting}\n- 身份：{identity}\n- 核心死线：{hook}\n- 关键能力：{skill}\n- 说明：本篇以丝袜/袜业/时尚职场为服装与行业元素，不含露骨描写。\n\n## 故事简介\n{intro}\n\n## 主要人物\n- **{heroine}**：在局里长大的女人，最会把输局掰成赢局。\n- **{hero}**：危险盟友，冷静强势，但总在最关键的时候站边。\n- **核心反派**：{villain}。\n- **关键助力**：{ally}。\n\n## 正文\n"]
    for chap in range(1, 21):
        parts.append(chapter_text(idx, chap, heroine, hero, setting, identity, hook, skill, villain, ally, tone))
    ending = pick(ENDINGS, idx, 31)
    parts.append(f"## 尾声\n\n最后一场灯亮起时，{heroine}终于明白，真正昂贵的不是她脚下踩过的台面，也不是柜台里摆着的成品，而是她在所有恶意里仍旧不肯塌下去的那口气。{ending}\n")
    return '\n'.join(parts)

