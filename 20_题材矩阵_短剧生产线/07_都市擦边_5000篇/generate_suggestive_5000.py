def make_story(idx: int) -> str:
    setting = pick(SETTINGS, idx, 3)
    identity = pick(IDENTITIES, idx, 5)
    hook = pick(HOOKS, idx, 7)
    skill = pick(SKILLS, idx, 11)
    villain = pick(VILLAINS, idx, 13)
    ally = pick(ALLIES, idx, 17)
    tone = pick(TONES, idx, 19)
    heroine = pick(NAMES_F, idx, 23)
    hero = pick(NAMES_M, idx, 29)
    title_core = f'{setting}{identity}{tone}'
    title = f'{title_core}第{idx:05d}篇'
    intro = f'{heroine}在{setting}里，是最容易被人误解、也最不该被低估的{identity}。她面前的第一道死线是“{hook}”，手里最能救命的本事是{skill}。可真正让她不得不下场的，不只是事业和名声，还有那个一次次把她逼到心跳失控边缘的人。'
    parts = [f"# 《{title}》\n\n## 基本信息\n- 题材：都市 / {tone}\n- 背景：{setting}\n- 身份：{identity}\n- 核心死线：{hook}\n- 关键能力：{skill}\n- 边界：强化暧昧、诱惑感与危险关系，但不写露骨细节。\n\n## 故事简介\n{intro}\n\n## 主要人物\n- **{heroine}**：越危险越冷静，越被误解越会翻盘。\n- **{hero}**：靠近她的人里，最危险也最让她失控的那个。\n- **核心反派**：{villain}。\n- **关键助力**：{ally}。\n\n## 正文\n"]
    for chap in range(1, 21):
        parts.append(chapter_text(idx, chap, heroine, hero, setting, identity, hook, skill, villain, ally, tone))
    ending = pick(ENDINGS, idx, 31)
    parts.append(f"## 尾声\n\n最后一盏灯熄下去时，{heroine}终于明白，真正让人沉下去的不是暧昧，不是目光，也不是贴得太近的呼吸，而是她明明知道危险，却还是在那个人靠近时，没有退。{ending}\n")
    return '\n'.join(parts)

