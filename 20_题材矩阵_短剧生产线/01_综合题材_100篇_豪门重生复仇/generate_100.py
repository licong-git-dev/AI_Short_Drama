def ep_line(n, protagonist, lead, villain, token):
    beats = {
        1: f'{protagonist}被当众羞辱，{token}第一次出现，{lead}看见异常。',
        2: f'{villain}逼{protagonist}认错，{protagonist}反手拿出证据。',
        3: f'{lead}发现{protagonist}身份不简单，饭局爆出第一层秘密。',
        18: f'{villain}抢先公开假证据，{protagonist}陷入全网围攻。',
        19: f'{lead}被迫站队，{protagonist}独自赴约。',
        20: f'{token}真正来源曝光，幕后黑手露出半张脸。',
        21: f'{protagonist}刚要翻盘，{lead}却被绑走。',
        22: f'{protagonist}闯入禁地，救下{lead}，反杀第一名黑手。',
        23: f'{villain}最后押上全部筹码，旧案真相浮出水面。',
        24: f'{protagonist}公开审判全局，却收到新的匿名挑战。',
    }
    if n in beats:
        return beats[n]
    cycle = [
        '反派升级压迫，主角用小证据拆局。',
        '助攻送来线索，主角发现旧案缺口。',
        '主角短暂落败，结尾拿到关键物证。',
        '当众打脸兑现，反派转向更狠手段。',
        '感情线升温，信任却被新谎言撕开。',
    ]
    return cycle[(n - 4) % len(cycle)]

