def make_story(idx: int) -> str:
    era = pick(ERAS, idx, 3)
    identity = pick(IDENTITIES, idx, 5)
    hook = pick(HOOKS, idx, 7)
    power = pick(POWERS, idx, 11)
    villain = pick(VILLAINS, idx, 13)
    ally = pick(ALLIES, idx, 17)
    tone = pick(TONES, idx, 19)
    heroine = pick(NAMES_F, idx, 23)
    hero = pick(NAMES_M, idx, 29)
    title_core = f'{era}{identity}{tone}'
    title = f'{title_core}第{idx:04d}篇'
    intro = f'{heroine}穿进{era}，成了人人都等着看笑话的{identity}。她面前的第一道死线是“{hook}”，唯一能倚仗的，是她带来的{power}。可真正决定她命运的，不是外挂，而是她能否在“{villain}”织成的大网里，靠自己杀出活路。'
    parts = [
        f"# 《{title}》\n\n## 基本信息\n- 主题：穿越 / {tone}\n- 背景：{era}\n- 身份：{identity}\n- 核心困境：{hook}\n- 关键能力：{power}\n\n## 故事简介\n{intro}\n\n## 主要人物\n- **{heroine}**：穿越者，擅长{power}，不信命，只信自己。\n- **{hero}**：危险盟友，看似冷淡，实则一直在试她能走多远。\n- **核心反派**：{villain}。\n- **关键助力**：{ally}。\n\n## 正文\n"
    ]
    for chap in range(1, 21):
        parts.append(chapter_text(idx, chap, heroine, hero, era, identity, hook, power, villain, ally, tone))
    ending = pick(ENDINGS, idx, 31)
    parts.append(f"## 尾声\n\n最后一局落下时，{heroine}终于明白，穿越并不是重来一次，而是逼一个人学会用新的方法赢回自己的人生。{ending}\n")
    return '\n'.join(parts)

